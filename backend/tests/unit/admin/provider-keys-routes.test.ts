describe('GET /admin/provider-keys', () => {
  it('returns an empty list when no keys are stored', async () => {
    const res = await makeApp().request('/admin/provider-keys', {
      headers: { Authorization: await adminBearer() },
    });
    expect(res.status).toBe(200);
    const body = (await res.json()) as { provider_keys: unknown[] };
    expect(body.provider_keys).toEqual([]);
  });

  it('returns masked entries with 8-char fixed mask, last4, version, and created_at', async () => {
    const app = makeApp();
    await app.request('/admin/provider-keys', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        Authorization: await adminBearer(),
      },
      body: JSON.stringify({
        provider: 'gemini',
        key: 'sk-live-abcdef1234XYZ',
      }),
    });

    const res = await app.request('/admin/provider-keys', {
      headers: { Authorization: await adminBearer() },
    });
    expect(res.status).toBe(200);
    const body = (await res.json()) as {
      provider_keys: Array<{
        provider: string;
        masked: string;
        last4: string;
        version: number;
        created_at: string;
        updated_at: string;
      }>;
    };
    expect(body.provider_keys).toHaveLength(1);
    const entry = body.provider_keys[0]!;
    expect(entry.provider).toBe('gemini');
    expect(entry.masked).toBe('********');
    expect(entry.masked.length).toBe(8);
    // last 4 of "sk-live-abcdef1234XYZ" => "4XYZ"
    expect(entry.last4).toBe('4XYZ');
    expect(entry.version).toBe(1);
    expect(typeof entry.created_at).toBe('string');
    expect(new Date(entry.created_at).toString()).not.toBe('Invalid Date');
  });

  it('omits any plaintext or ciphertext from the masked listing', async () => {
    const app = makeApp();
    const secret = 'super-secret-' + randomBytes(8).toString('hex');
    await app.request('/admin/provider-keys', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        Authorization: await adminBearer(),
      },
      body: JSON.stringify({ provider: 'groq', key: secret }),
    });

    const res = await app.request('/admin/provider-keys', {
      headers: { Authorization: await adminBearer() },
    });
    const text = await res.text();
    expect(text).not.toContain(secret);
    expect(text).not.toContain('ciphertext');
    expect(text).not.toContain('auth_tag');
    expect(text).not.toContain('nonce');
  });

  it('returns multiple stored keys ordered by canonical provider order', async () => {
    const app = makeApp();
    // Insert directly into the database with safe bytea values to avoid
    // pg-mem's intermittent bytea parsing issues with random ciphertext.
    await ctx.withClient(async (c) => {
      for (const provider of ['cerebras', 'gemini', 'deepseek', 'groq']) {
        const safeBytea = Buffer.from(`fake-${provider}`, 'utf8');
        await c.query(
          `INSERT INTO provider_keys (provider, ciphertext, nonce, auth_tag, last4, version)
           VALUES ($1, $2, $3, $4, $5, 1)`,
          [provider, safeBytea, Buffer.alloc(12, 0), Buffer.alloc(16, 0), '1234'],
        );
      }
    });

    const res = await app.request('/admin/provider-keys', {
      headers: { Authorization: await adminBearer() },
    });
    const body = (await res.json()) as {
      provider_keys: Array<{ provider: string }>;
    };
    expect(body.provider_keys.map((k) => k.provider)).toEqual([
      'gemini',
      'groq',
      'deepseek',
      'cerebras',
    ]);
  });
});

