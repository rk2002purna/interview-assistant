describe('PATCH /admin/provider-keys/:provider', () => {
  it('rotates the stored ciphertext and increments the version', async () => {
    const app = makeApp();
    const auth = { Authorization: await adminBearer(), 'Content-Type': 'application/json' };

    await app.request('/admin/provider-keys', {
      method: 'POST',
      headers: auth,
      body: JSON.stringify({ provider: 'gemini', key: 'orig-key-aaaa' }),
    });

    const res = await app.request('/admin/provider-keys/gemini', {
      method: 'PATCH',
      headers: auth,
      body: JSON.stringify({ key: 'rotated-bbbb' }),
    });
    expect(res.status).toBe(200);
    const body = (await res.json()) as {
      provider_key: { provider: string; version: number; last4: string };
    };
    expect(body.provider_key.version).toBe(2);
    expect(body.provider_key.last4).toBe('bbbb');

    const after = await readKeyMeta('gemini');
    expect(after!.version).toBe(2);
    expect(after!.last4).toBe('bbbb');
  });

  it('writes an audit row with previous_version, new version, and action=rotate', async () => {
    const app = makeApp();
    const auth = { Authorization: await adminBearer(), 'Content-Type': 'application/json' };

    await app.request('/admin/provider-keys', {
      method: 'POST',
      headers: auth,
      body: JSON.stringify({ provider: 'groq', key: 'orig-key-aaaa' }),
    });
    await app.request('/admin/provider-keys/groq', {
      method: 'PATCH',
      headers: auth,
      body: JSON.stringify({ key: 'rotated-bbbb' }),
    });

    const audits = await readAuditRows('provider_key_rotate');
    expect(audits).toHaveLength(1);
    const audit = audits[0]!;
    expect(audit.actor_user_id).toBe(ADMIN_ID);
    expect(audit.target_resource).toBe('provider_key:groq');
    expect(audit.metadata).toMatchObject({
      provider: 'groq',
      action: 'rotate',
      previous_version: 1,
      version: 2,
    });
  });

  it('produces a strictly increasing version across multiple rotations', async () => {
    const app = makeApp();
    const auth = { Authorization: await adminBearer(), 'Content-Type': 'application/json' };

    await app.request('/admin/provider-keys', {
      method: 'POST',
      headers: auth,
      body: JSON.stringify({ provider: 'deepseek', key: 'k1234' }),
    });
    for (let i = 0; i < 5; i++) {
      await app.request('/admin/provider-keys/deepseek', {
        method: 'PATCH',
        headers: auth,
        body: JSON.stringify({ key: `rot-${i}-1234` }),
      });
    }
    const row = await readKeyMeta('deepseek');
    expect(row!.version).toBe(6);
  });

  it('returns 404 when no key exists for that provider', async () => {
    const res = await makeApp().request('/admin/provider-keys/gemini', {
      method: 'PATCH',
      headers: {
        'Content-Type': 'application/json',
        Authorization: await adminBearer(),
      },
      body: JSON.stringify({ key: 'plain-1234' }),
    });
    expect(res.status).toBe(404);
    const body = (await res.json()) as { error: { code: string } };
    expect(body.error.code).toBe('provider_key_not_found');
  });

  it('rejects rotation with empty key and leaves prior key unchanged', async () => {
    const app = makeApp();
    const auth = { Authorization: await adminBearer(), 'Content-Type': 'application/json' };

    await app.request('/admin/provider-keys', {
      method: 'POST',
      headers: auth,
      body: JSON.stringify({ provider: 'gemini', key: 'orig-key-aaaa' }),
    });
    const before = await readKeyMeta('gemini');

    const res = await app.request('/admin/provider-keys/gemini', {
      method: 'PATCH',
      headers: auth,
      body: JSON.stringify({ key: '' }),
    });
    expect(res.status).toBe(400);
    const body = (await res.json()) as { error: { code: string } };
    expect(body.error.code).toBe('invalid_provider_key');

    const after = await readKeyMeta('gemini');
    expect(after!.version).toBe(1);
    expect(after!.last4).toBe(before!.last4);
  });

  it('rejects rotation with whitespace-padded key', async () => {
    const app = makeApp();
    const auth = { Authorization: await adminBearer(), 'Content-Type': 'application/json' };

    await app.request('/admin/provider-keys', {
      method: 'POST',
      headers: auth,
      body: JSON.stringify({ provider: 'gemini', key: 'orig-key-aaaa' }),
    });
    const res = await app.request('/admin/provider-keys/gemini', {
      method: 'PATCH',
      headers: auth,
      body: JSON.stringify({ key: ' has-leading' }),
    });
    expect(res.status).toBe(400);
  });

  it('rejects rotation against an unknown provider slug', async () => {
    const res = await makeApp().request('/admin/provider-keys/openai', {
      method: 'PATCH',
      headers: {
        'Content-Type': 'application/json',
        Authorization: await adminBearer(),
      },
      body: JSON.stringify({ key: 'plain-1234' }),
    });
    expect(res.status).toBe(400);
    const body = (await res.json()) as { error: { code: string } };
    expect(body.error.code).toBe('invalid_provider_key');
  });
});

