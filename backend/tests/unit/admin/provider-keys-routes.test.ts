async function adminBearer(): Promise<string> {
  const { token } = await signAccessToken({
    sub: ADMIN_ID,
    role: 'admin',
    clientId: CLIENT_ID,
  });
  return `Bearer ${token}`;
}

async function userBearer(): Promise<string> {
  const { token } = await signAccessToken({
    sub: USER_ID,
    role: 'user',
    clientId: CLIENT_ID,
  });
  return `Bearer ${token}`;
}

function makeApp() {
  return buildApp({ pool: ctx.pool });
}

interface AuditRow {
  actor_user_id: string;
  target_resource: string;
  event_type: string;
  outcome: string;
  metadata: Record<string, unknown>;
}

async function readAuditRows(eventType?: string): Promise<AuditRow[]> {
  return ctx.withClient(async (c) => {
    const sql = eventType
      ? `SELECT actor_user_id, target_resource, event_type, outcome, metadata
           FROM audit_log
          WHERE event_type = $1
          ORDER BY ts ASC`
      : `SELECT actor_user_id, target_resource, event_type, outcome, metadata
           FROM audit_log
          ORDER BY ts ASC`;
    const result = eventType
      ? await c.query<AuditRow & { metadata: unknown }>(sql, [eventType])
      : await c.query<AuditRow & { metadata: unknown }>(sql);
    return result.rows.map((r) => ({
      actor_user_id: r.actor_user_id,
      target_resource: r.target_resource,
      event_type: r.event_type,
      outcome: r.outcome,
      metadata:
        typeof r.metadata === 'string'
          ? (JSON.parse(r.metadata) as Record<string, unknown>)
          : (r.metadata as Record<string, unknown>),
    }));
  });
}

/**
 * Read the metadata columns of a provider_keys row.
 *
 * Intentionally omits the bytea columns (ciphertext / nonce / auth_tag)
 * because pg-mem's pg adapter does not round-trip bytea Buffers
 * cleanly under all conditions; the encrypt/decrypt round trip is
 * already validated in `tests/unit/ai/keys.test.ts` against the same
 * `aes-gcm` module these routes use, and against the same
 * `provider_keys` schema.
 */
async function readKeyMeta(provider: string) {
  return ctx.withClient(async (c) => {
    const r = await c.query<{
      provider: string;
      last4: string;
      version: number;
    }>(
      `SELECT provider, last4, version
         FROM provider_keys
        WHERE provider = $1`,
      [provider],
    );
    return r.rows[0];
  });
}

async function countKeyRows(provider: string): Promise<number> {
  return ctx.withClient(async (c) => {
    const r = await c.query<{ count: string }>(
      `SELECT COUNT(*)::text AS count FROM provider_keys WHERE provider = $1`,
      [provider],
    );
    return Number(r.rows[0]?.count ?? 0);
  });
}

